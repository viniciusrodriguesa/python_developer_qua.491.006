
nome = input("Informe seu nome: ").title()
print(f"Seu nome é: {nome}.")

texto = input("Digite uma frase: ").capitalize()
print(f"Frase: {texto}.")

minuscula = input("Digite um texto totalmente em caixa baixa: ").upper()
print(f"Texto em maiúsculo: {minuscula}.")

maiuscula = input("Digite um texto totalmente em caixa alta: ").lower()
print(f"Texto em minúsculo: {maiuscula}.")
